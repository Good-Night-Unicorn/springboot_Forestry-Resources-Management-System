<script>
  import config from '@/config/config'
  import menu from '@/config/menu'
  import Vue from 'vue'
  export default {
    //数据集合
    data() {
      return {
        title: '个人中心',
        baseUrl: config.baseUrl,
        sessionForm: {},
		passwordForm: {},
		passwordRules: {
			password: [
				{
					required: true,
					message: "密码不能为空",
					trigger: "blur"
				}
			],
			newpassword: [
				{
					required: true,
					message: "新密码不能为空",
					trigger: "blur"
				}
			],
			repassword: [
				{
					required: true,
					message: "确认密码不能为空",
					trigger: "blur"
				}
			]
		},
        rules: {},
		menuList: [],
        disabled: false,
        uploadUrl: config.baseUrl + 'file/upload',
        imageUrl: '',
        headers: {Token: localStorage.getItem('frontToken')},
        userTableName: localStorage.getItem('UserTableName'),
        dynamicProp: {},
      }
    },
    created() {
		let menus = menu.list()
		for(let x in menus){
			if(menus[x].tableName == this.userTableName){
				for(let i in menus[x].backMenu){
					if(menus[x].backMenu[i].menu=='考试管理'){
						menus[x].backMenu[i].splice(i,1)
					}
				}
				this.menuList = menus[x].backMenu
			}
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'qiyemingcheng', null);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'mima', null);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'fuzeren', null);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'xingbie', null);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'shouji', null);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.sessionForm, 'touxiang', null);
		}

		if ('kaifaqiye' == this.userTableName&&this.rules['qiyemingcheng']){
			this.rules['qiyemingcheng'].push({ required: true, message: '请输入企业名称', trigger: 'blur' })
		}else if('kaifaqiye' == this.userTableName&&!this.rules['qiyemingcheng']) {
			this.$set(this.rules, 'qiyemingcheng', [{ required: true, message: '请输入企业名称', trigger: 'blur' }]);
		}
		if ('kaifaqiye' == this.userTableName&&this.rules['mima']){
			this.rules['mima'].push({ required: true, message: '请输入密码', trigger: 'blur' })
		}else if('kaifaqiye' == this.userTableName&&!this.rules['mima']) {
			this.$set(this.rules, 'mima', [{ required: true, message: '请输入密码', trigger: 'blur' }]);
		}
		if ('kaifaqiye' == this.userTableName&&this.rules['fuzeren']){
			this.rules['fuzeren'].push({ required: true, message: '请输入负责人', trigger: 'blur' })
		}else if('kaifaqiye' == this.userTableName&&!this.rules['fuzeren']) {
			this.$set(this.rules, 'fuzeren', [{ required: true, message: '请输入负责人', trigger: 'blur' }]);
		}
		if ('kaifaqiye' == this.userTableName&&this.rules['xingbie']){
			this.rules['xingbie'].push({ required: true, message: '请输入性别', trigger: 'blur' })
		}else if('kaifaqiye' == this.userTableName&&!this.rules['xingbie']) {
			this.$set(this.rules, 'xingbie', [{ required: true, message: '请输入性别', trigger: 'blur' }]);
		}
		if ('kaifaqiye' == this.userTableName) {
			this.$set(this.rules, 'shouji', [{ required: false, validator: this.$validate.isMobile, trigger: 'blur' }]);
		}
		if ('kaifaqiye' == this.userTableName&&this.rules['shouji']){
			this.rules['shouji'].push({ required: true, message: '请输入手机', trigger: 'blur' })
		}else if('kaifaqiye' == this.userTableName&&!this.rules['shouji']) {
			this.$set(this.rules, 'shouji', [{ required: true, message: '请输入手机', trigger: 'blur' }]);
		}

      this.init();
      this.sessionForm = JSON.parse(localStorage.getItem('sessionForm'))
    },
    //方法集合
    methods: {
      init() {
        if ('kaifaqiye' == this.userTableName) {
          this.dynamicProp.xingbie = '男,女'.split(',');
        }
      },
	  setSession(){
		  localStorage.setItem('sessionForm',JSON.stringify(this.sessionForm))
	  },
      onSubmit(formName) {
		if(`kaifaqiye` == this.userTableName && this.sessionForm.touxiang!=null){
			this.sessionForm.touxiang = this.sessionForm.touxiang.replace(new RegExp(this.$config.baseUrl,"g"),"");
		}
        this.$refs[formName].validate((valid) => {
			if (valid) {
				this.$http.post(this.userTableName + '/update', this.sessionForm).then(res => {
					if (res.data.code == 0) {
						this.setSession()
						this.$message({
							message: '更新成功',
							type: 'success',
							duration: 1500
						});
					}
				});
			} else {
				return false;
			}
        });
      },
      kaifaqiyetouxiangHandleAvatarSuccess(fileUrls) {
        this.sessionForm.touxiang = fileUrls;
      },
      handleClick(tab, event) {
        switch(event.target.outerText) {
          case '个人中心':
            tab.$router.push('/index/center');
            break;
          case '修改密码':
            this.passwordForm = {
				password: '',
				newpassword: '',
				repassword: '',
			}
			this.$forceUpdate()
            break;
          case '我的收藏':
            localStorage.setItem('storeupType', 1);
            tab.$router.push('/index/storeup');
            break;
		  default:
		  	tab.$router.push(`/index/${tab.name}?centerType=1`);
        }

        this.title = event.target.outerText;
      },
	  async updatePassword(){
			this.$refs["passwordForm"].validate(async valid => {
				if (valid) {
					var password = "";
					if (this.sessionForm.mima) {
						password = this.sessionForm.mima;
					} else if (this.sessionForm.password) {
						password = this.sessionForm.password;
					}
					if (this.userTableName == 'kaifaqiye') {
					}
					if (this.passwordForm.password != password) {
						this.$message.error("原密码错误");
						return;
					}
					if (this.passwordForm.newpassword != this.passwordForm.repassword) {
						this.$message.error("两次密码输入不一致");
						return;
					}
					if (this.passwordForm.newpassword == this.passwordForm.password) {
						this.$message.error("新密码与原密码相同！");
						return;
					}
					this.sessionForm.password = this.passwordForm.newpassword;
					this.sessionForm.mima = this.passwordForm.newpassword;
					this.$http.post(`${this.userTableName}/update`,this.sessionForm).then(({data})=>{
						if (data && data.code === 0) {
							this.$message({
								message: "修改密码成功,下次登录系统生效",
								type: "success",
								duration: 1500,
								onClose: () => {
								}
							});
							this.setSession()
						} else {
							this.$message.error(data.msg);
						}
					});
				}
			})
	  },
      logout() {
        localStorage.clear();
        Vue.http.headers.common['Token'] = "";
        this.$router.push('/index/home');
        this.activeIndex = '0'
        localStorage.setItem('keyPath', this.activeIndex)
        this.$forceUpdate()
        this.$message({
            message: '登出成功',
            type: 'success',
            duration: 1500,
        });
      },
	  hasBack(name){
		  switch(name){
			case '我的收藏管理':
				return false
				break;
			default:
				return true
		  }
	  }
    }
  }
</script>

